import turtle

turtle.setup(500, 500, 200, 200)
turtle.penup()
turtle.fd(-50)
turtle.pendown()
turtle.pensize(10)
turtle.pencolor("purple")
turtle.fd(100)
turtle.seth(120)
turtle.fd(100)
turtle.seth(240)
turtle.fd(200)
turtle.seth(0)
turtle.fd(200)
turtle.seth(120)
turtle.fd(100)
turtle.seth(240)
turtle.fd(100)
turtle.seth(120)
turtle.fd(100)
turtle.seth(0)

turtle.done()
